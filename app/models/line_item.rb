class LineItem < ApplicationRecord
  belongs_to :cart
  belongs_to :item

  def total
    item.price * quantity
  end

  # DELETE /line_items/1
  # DELETE /line_items/1.json
  def destroy
    @line_item.destroy
    respond_to do |format|
      format.html { redirect_to line_items_url, notice: 'Line item was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

end
